# What is Object Oriented Programing?.
# This allows us go beyond data types which come with python built in and create our own data types

# The key here is we are able to create our own datatypes with different attributes and methods


# OOP is a paradigm that is,it's a way for us to think about our code and structure our code in a way thats's easier to maintain, extend and write.

# An example is a delivery drone sent from amazon. Parts such as the propellars, signaling, the claw of the drone and the camera are all coded separately an act as objects .

# Hence this breaks up functionality and the data into different parts which model the real world and are combined afterwards.

